#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	char	*arr;
	size_t	i;

	i = 0;
	c %= 256;
	arr = (char*)b;
	while (i < len)
	{
		arr[i] = c;
		i++;
	}
	return (arr);
}
